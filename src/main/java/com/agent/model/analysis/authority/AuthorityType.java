package com.agent.model.analysis.authority;

/**
 * Enumeration of legal authority types.
 */
public enum AuthorityType {
    /**
     * Statutory law, codes, ordinances
     */
    STATUTE,
    
    /**
     * Court opinions and case decisions
     */
    CASE_LAW,
    
    /**
     * Practice guides, manuals, best practices
     */
    PRACTICE_GUIDE,
    
    /**
     * Legal commentary, restatements, notes
     */
    COMMENTARY,
    
    /**
     * Other types of legal authority
     */
    OTHER
}
